-- Function to get battery status
local function update_battery()
    awful.spawn.easy_async_with_shell(
        "cat /sys/class/power_supply/BAT*/capacity 2>/dev/null || echo 'N/A'",
        function(capacity)
            awful.spawn.easy_async_with_shell(
                "cat /sys/class/power_supply/BAT*/status 2>/dev/null || echo 'N/A'",
                function(status)
                    capacity = capacity:gsub("%s+", "")
                    status = status:gsub("%s+", "")

                    local icon = " "
                    if status:match("Charging") then
                        icon = " "
                    elseif tonumber(capacity) and tonumber(capacity) <= 20 then
                        icon = " "
                    end

                    battery_widget.txt.text = string.format("%s %s%%", icon, capacity)
                end
            )
        end
    )
end

-- Update battery status on a timer
gears.timer {
    timeout = 60,
    call_now = true,
    autostart = true,
    callback = update_battery
}


